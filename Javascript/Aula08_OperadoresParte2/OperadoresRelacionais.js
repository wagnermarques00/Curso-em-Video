//Operadores Relacionais
5 > 2; // 5 é maior que 2 -> true
7 < 4; // 7 é menor que 4 -> false
8 >= 8; // 8 é maior ou igual a 8 -> true
9 <= 7; // 9 é menor ou igual a 7 -> false
5 == 5; // 5 é igual a 5 -> true
4 != 4; // 4 é diferente de 4 -> false

//Operação de igualdade (identidade)
5 == 5; // 5 é igual a 5 -> true
5 != 5; // 5 é diferente de 5 -> false
5 == "5"; // 5 é igual a "5" -> true
5 != "5"; // 5 é diferente de "5" -> false

5 === "5"; // 5 é identico a "5" -> false
5 !== "5"; // 5 não é identico a "5" -> true
5 === 5; // 5 é identico a 5 -> true
5 !== 5; // 5 não é identico a 5 -> false